bool RustLexer::sempred(RuleContext *context, size_t ruleIndex, size_t predicateIndex) {
  switch (ruleIndex) {
    case 107: return FloatLitSempred(antlrcpp::downCast<antlr4::RuleContext *>(context), predicateIndex);

  default:
    break;
  }
  return true;
}


bool RustLexer::FloatLitSempred(antlr4::RuleContext *_localctx, size_t predicateIndex) {
  switch (predicateIndex) {
    case 0: return 
            /* dot followed by another dot is a range, not a float */
            _input->LA(1) != '.' &&
            /* dot followed by an identifier is an integer with a function call, not a float */
            _input->LA(1) != '_' &&
            !(_input->LA(1) >= 'a' && _input->LA(1) <= 'z') &&
            !(_input->LA(1) >= 'A' && _input->LA(1) <= 'Z')
        ;

  default:
    break;
  }
  return true;
}


